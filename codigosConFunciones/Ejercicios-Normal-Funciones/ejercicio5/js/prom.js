function promedio(pnota1, pnota2, pnota3){ // parametros

        let prom;
        let nota1 = pnota1;
        let nota2 = pnota2;
        let nota3 = pnota3;

        prom = (nota1+nota2+nota3)/3;

        return prom;
}

