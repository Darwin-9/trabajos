function numeros(pnumUno,pnumDos){
    let numero;
    let numUno = pnumUno;
    let numDos = pnumDos;

    if(numUno==numDos){
        numero = "Son iguales"
    } else if(numUno>numDos){
        numero = "Numero 1 es mayor"
    } else{
        numero = "Numero 2 es mayor"
    }

    return numero;
}

